import config from "../../config.js";

export default {
  name: "help",
  aliases: ["ajuda", "h", "commands"],
  description: "Mostra todos os comandos dispon√≠veis ou informa√ß√µes sobre um comando espec√≠fico com pagina√ß√£o.",
  category: "core",
  usage: "help [comando] [p√°gina]",
  cooldown: 3000,
  
  async execute(client, message, args) {
    try {
      // Se um comando espec√≠fico foi solicitado
      if (args.length > 0 && isNaN(args[0])) {
        const commandName = args[0].toLowerCase();
        const command = client.commands.get(commandName) || 
                       client.commands.find(cmd => cmd.aliases && cmd.aliases.includes(commandName));
        
        if (!command) {
          const embed = {
            color: config.colors.error,
            title: `${config.emojis.error} Comando n√£o encontrado`,
            description: `O comando **${commandName}** n√£o existe.\n\nUse \`${config.prefix}help\` para ver todos os comandos dispon√≠veis.`,
          };
          return await message.reply({ embeds: [embed] });
        }
        
        // Mostrar informa√ß√µes detalhadas do comando
        const embed = {
          color: config.colors.primary,
          title: `${config.emojis.info} Comando: ${command.name}`,
          fields: [
            {
              name: "üìù Descri√ß√£o",
              value: command.description || "Sem descri√ß√£o dispon√≠vel",
              inline: false,
            },
            {
              name: "üí¨ Uso",
              value: `\`${config.prefix}${command.usage || command.name}\``,
              inline: true,
            },
            {
              name: "üìÇ Categoria",
              value: command.category || "Core",
              inline: true,
            },
            {
              name: "‚è±Ô∏è Cooldown",
              value: `${(command.cooldown || 0) / 1000}s`,
              inline: true,
            },
          ],
        };
        
        if (command.aliases && command.aliases.length > 0) {
          embed.fields.push({
            name: "üîó Aliases",
            value: command.aliases.map(alias => `\`${alias}\``).join(", "),
            inline: false,
          });
        }
        
        if (command.permissions) {
          embed.fields.push({
            name: "üîí Permiss√µes",
            value: command.permissions.join(", "),
            inline: false,
          });
        }
        
        if (command.ownerOnly) {
          embed.fields.push({
            name: "üëë Restrito",
            value: "Apenas o desenvolvedor",
            inline: false,
          });
        }
        
        return await message.reply({ embeds: [embed] });
      }
      
      // Parse da p√°gina
      const page = parseInt(args[0]) || 1;
      
      // Mostrar lista de todos os comandos com pagina√ß√£o
      const categories = this.organizeCommandsByCategory(client.commands);
      const categoriesPerPage = 2; // Reduzir para 2 categorias por p√°gina para melhor legibilidade
      const totalCategories = Object.keys(categories).length;
      const totalPages = Math.ceil(totalCategories / categoriesPerPage);
      
      if (page < 1 || page > totalPages) {
        const embed = {
          color: config.colors.warning,
          title: `${config.emojis.warning} P√°gina Inv√°lida`,
          description: `A p√°gina deve estar entre 1 e ${totalPages}.\n\nUse \`${config.prefix}help\` sem argumentos para ver a primeira p√°gina.`,
        };
        return await message.reply({ embeds: [embed] });
      }
      
      const embed = this.createPaginatedHelpEmbed(categories, page, totalPages, categoriesPerPage);
      const sentMessage = await message.reply({ embeds: [embed] });
      
      // Adicionar rea√ß√µes para navega√ß√£o se houver m√∫ltiplas p√°ginas
      if (totalPages > 1) {
        try {
          await sentMessage.react('‚¨ÖÔ∏è');
          await sentMessage.react('‚û°Ô∏è');
          await sentMessage.react('üìã'); // Para mostrar √≠ndice
          
          // Collector para navega√ß√£o
          const filter = (reaction, user) => {
            return ['‚¨ÖÔ∏è', '‚û°Ô∏è', 'üìã'].includes(reaction.emoji.name) && user.id === message.author.id && !user.bot;
          };
          
          const collector = sentMessage.createReactionCollector({ filter, time: 300000 }); // 5 minutos
          
          let currentPage = page;
          
          collector.on('collect', async (reaction, user) => {
            try {
              if (reaction.emoji.name === '‚¨ÖÔ∏è' && currentPage > 1) {
                currentPage--;
                const newEmbed = this.createPaginatedHelpEmbed(categories, currentPage, totalPages, categoriesPerPage);
                await sentMessage.edit({ embeds: [newEmbed] });
              } else if (reaction.emoji.name === '‚û°Ô∏è' && currentPage < totalPages) {
                currentPage++;
                const newEmbed = this.createPaginatedHelpEmbed(categories, currentPage, totalPages, categoriesPerPage);
                await sentMessage.edit({ embeds: [newEmbed] });
              } else if (reaction.emoji.name === 'üìã') {
                // Mostrar √≠ndice de categorias
                const indexEmbed = this.createCategoryIndexEmbed(categories, totalPages);
                await sentMessage.edit({ embeds: [indexEmbed] });
                
                // Voltar para a p√°gina atual ap√≥s 10 segundos
                setTimeout(async () => {
                  try {
                    const originalEmbed = this.createPaginatedHelpEmbed(categories, currentPage, totalPages, categoriesPerPage);
                    await sentMessage.edit({ embeds: [originalEmbed] });
                  } catch (error) {
                    console.log('Erro ao voltar ao embed original:', error.message);
                  }
                }, 10000);
              }
              
              // Remover rea√ß√£o do usu√°rio
              try {
                await reaction.users.remove(user.id);
              } catch (error) {
                // Falha silenciosa se n√£o tiver permiss√£o para remover rea√ß√µes
                console.log('Sem permiss√£o para remover rea√ß√£o do usu√°rio');
              }
            } catch (error) {
              console.error('Erro ao processar rea√ß√£o:', error);
            }
          });
          
          collector.on('end', () => {
            // Tentar remover todas as rea√ß√µes ao final
            sentMessage.reactions.removeAll().catch(() => {
              console.log('Sem permiss√£o para remover todas as rea√ß√µes');
            });
          });
          
        } catch (error) {
          console.error('Erro ao adicionar rea√ß√µes:', error);
          // Se n√£o conseguir adicionar rea√ß√µes, ainda funciona com comandos de texto
          console.log('Pagina√ß√£o funcionar√° apenas via comandos de texto (m.help 2, etc.)');
        }
      }
      
    } catch (error) {
      console.error("Erro no comando help:", error);
      
      const errorEmbed = {
        color: config.colors.error,
        title: `${config.emojis.error} Erro`,
        description: "Ocorreu um erro ao mostrar a ajuda. Tente novamente.",
      };
      
      await message.reply({ embeds: [errorEmbed] });
    }
  },
  
  organizeCommandsByCategory(commands) {
    const categories = {};
    
    commands.forEach(command => {
      const category = command.category || "outros";
      if (!categories[category]) {
        categories[category] = [];
      }
      categories[category].push(command);
    });
    
    // Ordenar comandos dentro de cada categoria
    Object.keys(categories).forEach(category => {
      categories[category].sort((a, b) => a.name.localeCompare(b.name));
    });
    
    return categories;
  },
  
  createPaginatedHelpEmbed(categories, page, totalPages, categoriesPerPage) {
    const categoryEntries = Object.entries(categories);
    const startIndex = (page - 1) * categoriesPerPage;
    const endIndex = startIndex + categoriesPerPage;
    const currentCategories = categoryEntries.slice(startIndex, endIndex);
    
    const totalCommands = Object.values(categories).flat().length;
    
    const embed = {
      color: config.colors.primary,
      title: `${config.emojis.help} Central de Ajuda - MaryBot`,
      description: [
        `Use \`${config.prefix}help [comando]\` para informa√ß√µes detalhadas sobre um comando espec√≠fico.`,
        `Use \`${config.prefix}help [p√°gina]\` para navegar entre p√°ginas.`,
        ``,
        `**üè∞ Comandos Principais**: \`dungeon\`, \`inv\`, \`equip\`, \`stats\`, \`loot\`, \`shop\``
      ].join('\n'),
      fields: [],
      footer: {
        text: `P√°gina ${page}/${totalPages} ‚Ä¢ Total: ${totalCommands} comandos ‚Ä¢ Use ‚¨ÖÔ∏è‚û°Ô∏è para navegar`,
      },
      timestamp: new Date().toISOString(),
    };
    
    // Adicionar cada categoria da p√°gina atual
    currentCategories.forEach(([categoryName, commands]) => {
      const categoryEmoji = this.getCategoryEmoji(categoryName);
      
      // Mostrar todos os comandos sem abrevia√ß√£o
      const commandList = commands
        .map(cmd => `\`${cmd.name}\``)
        .join(", ");
      
      embed.fields.push({
        name: `${categoryEmoji} ${categoryName.toUpperCase()} (${commands.length} comandos)`,
        value: commandList,
        inline: false,
      });
    });
    
    // Adicionar field com informa√ß√µes √∫teis apenas na primeira p√°gina
    if (page === 1) {
      embed.fields.push({
        name: "üí° Come√ßando no MaryBot",
        value: [
          `‚Ä¢ \`${config.prefix}dungeon start\` - Inicie sua aventura`,
          `‚Ä¢ \`${config.prefix}daily\` - Colete moedas di√°rias`,
          `‚Ä¢ \`${config.prefix}profile\` - Veja seu perfil`,
          `‚Ä¢ \`${config.prefix}help <comando>\` - Ajuda espec√≠fica`
        ].join('\n'),
        inline: false,
      });
    } else {
      // Nas outras p√°ginas, mostrar dicas de navega√ß√£o
      embed.fields.push({
        name: "üß≠ Navega√ß√£o",
        value: [
          `‚Ä¢ Use ‚¨ÖÔ∏è ‚û°Ô∏è para navegar entre p√°ginas`,
          `‚Ä¢ Use üìã para ver o √≠ndice completo`,
          `‚Ä¢ Use \`${config.prefix}help <comando>\` para detalhes`,
          `‚Ä¢ Use \`${config.prefix}help 1\` para voltar ao in√≠cio`
        ].join('\n'),
        inline: false,
      });
    }
    
    return embed;
  },
  
  createCategoryIndexEmbed(categories, totalPages) {
    const embed = {
      color: config.colors.primary,
      title: `üìã √çndice Completo de Categorias`,
      description: "Vis√£o geral de todas as categorias de comandos dispon√≠veis no MaryBot:",
      fields: [],
      footer: {
        text: `Voltando para a ajuda principal em 10 segundos...`,
      },
      timestamp: new Date().toISOString(),
    };
    
    Object.entries(categories).forEach(([categoryName, commands], index) => {
      const categoryEmoji = this.getCategoryEmoji(categoryName);
      const commandCount = commands.length;
      const topCommands = commands.slice(0, 4).map(cmd => cmd.name).join(", ");
      
      embed.fields.push({
        name: `${categoryEmoji} ${categoryName.toUpperCase()}`,
        value: `**${commandCount} comandos**\nDestaque: ${topCommands}${commandCount > 4 ? '...' : ''}`,
        inline: true,
      });
    });
    
    return embed;
  },
  
  getCategoryEmoji(category) {
    const emojis = {
      "core": "‚öôÔ∏è",
      "dungeon": "üè∞",
      "anime": "üéå",
      "economy": "üí∞",
      "admin": "üõ†Ô∏è",
      "fun": "üéÆ",
      "utility": "üîß",
      "outros": "üìÅ",
    };
    
    return emojis[category.toLowerCase()] || "üìÅ";
  },
};